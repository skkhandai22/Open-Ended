def check_gender(gender):
    if gender == 'Male':
        return 1
    else:
        return 0

def check_marriage_status(marriage_status):
    if marriage_status == 'Yes':
        return 1
    else: 
        return 0
    
def check_dependents(dependent):
    if dependent == 'Yes':
        return 1
    else: 
        return 0
    
def check_qualification(qual):
    if qual == 'Graduate':
        return 0
    else: 
        return 1

def check_self_employed(emp):
    if emp == 'Yes':
        return 1
    else: 
        return 0

def check_debt(debt):
    if debt == 'Yes':
        return 1
    else: 
        return 0
    
